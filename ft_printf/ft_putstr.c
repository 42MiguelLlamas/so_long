#include "ft_printf.h"

void	ft_putstr(char const *s, size_t *count)
{
	int		i;

	if (!s)
		s = "(null)";
	i = 0;
	while (s[i])
	{
		ft_putchar(s[i], count);
		i++;
	}
}
